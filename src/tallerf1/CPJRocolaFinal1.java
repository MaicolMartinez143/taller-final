    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
       String tituloBuscado = txtTitulo.getText();
        
        Cancion cancionEncontrada = rocola.buscarCancion(tituloBuscado);
        
        if (cancionEncontrada != null) {
           Resultado.setText("Cancion encontrada: " + cancionEncontrada);
        } else {
           Resultado.setText("Cancion no encontrada");
        }
       
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnreproducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreproducirActionPerformed
   String titulo = txtTitulo.getText(); 
        Cancion cancion = rocola.buscarCancion(titulo);
        String msj;

        if (cancion != null) {
            cancion.reproducir(); 
            msj= cancion.getMsj();
           Resultado.setText( msj+"\n" + "Reproducciones: " + cancion.contadorReproducciones);
        } else {
            Resultado.setText("Canción no encontrada: " + titulo);
        }
    
    
    }//GEN-LAST:event_btnreproducirActionPerformed

