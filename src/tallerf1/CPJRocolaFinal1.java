    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
         String titulo = txtTitulo.getText();
    String artista = txtArtista.getText();
    double duracion = Double.parseDouble(txtDuracion.getText());

    
    Cancion cancion = new Cancion(titulo, artista, duracion);
    
    String msj = rocola.agregarCancion(cancion.getTitulo(), cancion.getArtista(), cancion.getDuracion());
    
   
    Resultado.setText(msj);
    
    
    actualizarTextoCanciones();
    }//GEN-LAST:event_btnagregarActionPerformed

